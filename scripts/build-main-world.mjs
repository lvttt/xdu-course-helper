import { mkdir } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build, context } from 'esbuild';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

const banner = {
    js: '/* AUTO-GENERATED by scripts/build-main-world.mjs. DO NOT EDIT. */',
};

const targets = [
    {
        entry: path.join(rootDir, 'src/content/entries/index.entry.js'),
        outfile: path.join(rootDir, 'src/content/index.js'),
        globalName: 'XduCourseHelperContentIndex',
    },
    {
        entry: path.join(rootDir, 'src/content/entries/early-hook.entry.js'),
        outfile: path.join(rootDir, 'src/content/early-hook.js'),
        globalName: 'XduCourseHelperEarlyHook',
    },
];

function createBuildOptions(target) {
    return {
        entryPoints: [target.entry],
        outfile: target.outfile,
        bundle: true,
        format: 'iife',
        platform: 'browser',
        target: ['chrome114'],
        minify: false,
        sourcemap: false,
        legalComments: 'none',
        charset: 'utf8',
        globalName: target.globalName,
        banner,
        logLevel: 'info',
    };
}

async function runBuild() {
    for (const target of targets) {
        await mkdir(path.dirname(target.outfile), { recursive: true });
        await build(createBuildOptions(target));
    }
}

async function runWatch() {
    const contexts = [];
    for (const target of targets) {
        await mkdir(path.dirname(target.outfile), { recursive: true });
        const ctx = await context(createBuildOptions(target));
        await ctx.watch();
        contexts.push(ctx);
    }

    process.on('SIGINT', async () => {
        await Promise.all(contexts.map((ctx) => ctx.dispose()));
        process.exit(0);
    });

    console.log('[build-main-world] watching...');
}

if (process.argv.includes('--watch')) {
    await runWatch();
} else {
    await runBuild();
}

